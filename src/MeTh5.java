public class MeTh5 {
    public static void main(String[] args) {
        //casting
        //explicit
         int t2 = (int) 4.5;
         float f4 = (float) 5.6;

         //implicit
         double d4 = 4.5 + 3;
         double d6 = 400;

         double d = 4.3;  // is 4.3
         int i = (int) d; // 4  explicit casting

        double r = 1.0f/2; // implicit casting
        System.out.println(r);

        System.out.println(1.0f/2); //implicit casting
    }
}
